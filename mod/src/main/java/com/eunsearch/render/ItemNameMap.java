    public static String getCnName(String itemId) {
        if (itemId == null) return "???";
        String cn = CN.get(itemId);
        if (cn != null) return cn;
        int colon = itemId.indexOf(':');
        String path = colon >= 0 ? itemId.substring(colon + 1) : itemId;
        String readable = path.replace('_', ' ');
        if (readable.length() > 0) {
            readable = Character.toUpperCase(readable.charAt(0)) + readable.substring(1);
        }
        return readable;
    }

    public static String getItemId(String cnOrId) {
        if (cnOrId == null || cnOrId.isEmpty()) return null;
        // direct match
        if (CN.containsKey("minecraft:" + cnOrId)) return "minecraft:" + cnOrId;
        if (cnOrId.contains(":") && CN.containsKey(cnOrId)) return cnOrId;
        // reverse lookup by Chinese name
        for (Map.Entry<String, String> e : CN.entrySet()) {
            if (e.getValue().equals(cnOrId)) return e.getKey();
        }
        // auto-prefix
        if (!cnOrId.contains(":")) return "minecraft:" + cnOrId.toLowerCase();
        return cnOrId.toLowerCase();
    }

